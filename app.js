function renderNav() {
  const tabs = [
    { id: "home", title: "–ì–ª–∞–≤–Ω–∞—è" },
    ...FUNCTIONS.map((item) => ({ id: item.id, title: item.title }))
  ];

  nav.innerHTML = "";
  tabs.forEach((tab) => {
    const button = document.createElement("button");
    button.textContent = tab.title;
    button.className = tab.id === state.activeTab ? "active" : "";
    button.addEventListener("click", () => {
      state.activeTab = tab.id;
      renderNav();
      renderApp();
    });
    nav.appendChild(button);
  });
}

function renderApp() {
  app.innerHTML = "";
  if (state.activeTab === "home") {
    renderHome();
    return;
  }

  const section = FUNCTIONS.find((item) => item.id === state.activeTab);
  if (!section) {
    return;
  }

  const panel = document.createElement("section");
  panel.className = "panel";
  panel.innerHTML = `
    <h2>${section.title}</h2>
    <p class="meta">${section.description}</p>
  `;

  panel.appendChild(renderSectionTabs(section.id));

  if (getActiveSubTab(section.id) === "life") {
    panel.appendChild(renderLifeSection(section));
  } else {
    section.tasks.forEach((task) => {
      panel.appendChild(renderTask(section, task));
    });
    panel.appendChild(renderFunctionProgress(section.id));
  }
  app.appendChild(panel);
}

